/* Matrices = Arrays
¡Bienvenidos! En este capítulo, aprenderemos a almacenar múltiples valores en una sola variable 
llamada matriz.



Las matrices son variables que pueden contener múltiples valores:

array = ["Leonardo", "Michaelangelo", "Donatello", "Raphael"];

Se crean utilizando corchetes [y ]. Y los elementos dentro están separados por ,comas.

Los elementos utilizados en nuestra matriz pueden ser una combinación de tipos de datos o un solo 
tipo. Por ejemplo, las dos matrices siguientes son válidas:

let lotteryNumbers = [13, 36, 45, 57, 67, 14];       // call us if you have these numbers!
const lettersAndNumbers = ["a", "b", "c", 1, 2, 3];

Podemos definir matrices con leto const. Puedes repasar este ejercicio para repasar.

Instrucciones
¡La ciudad de Nueva York está formada por cinco distritos que están llenos de destinos divertidos!

Digamos que estamos planeando un viaje a Nueva York y queremos visitar al menos un lugar de cada uno 
de los cinco distritos:

"🗽 Manhattan - Washington Square Park"
"🏟️ The Bronx - Yankee Stadium"
"🎡 Brooklyn - Coney Island"
"🧧 Queens - Flushing"
"🌉 Staten Island - Historic Richmond Town"
¡Podemos almacenar toda esta información en una matriz!

Copie y pegue lo siguiente en el editor: */

let myDestinations = [
    "🗽 Manhattan - Washington Square Park",
    "🏟️ The Bronx - Yankee Stadium",
    "🎡 Brooklyn - Coney Island",
    "🧧 Queens - Flushing",
    "🌉 Staten Island - Historic Richmond Town"
];

console.log(myDestinations);

